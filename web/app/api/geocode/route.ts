// ── 역명 검색 ──────────────────────
function findStation(query: string): { name: string; lat: number; lng: number } | null {
  // "역" 붙이거나 빼서 검색
  const cleaned = query.replace(/역$/, '').replace(/\s/g, '');
  for (const [name, coords] of Object.entries(SUBWAY_STATIONS)) {
    if (cleaned === name || cleaned === name + '역' || name === cleaned + '역' || cleaned === name.replace(/역$/, '')) {
      return { name: name + '역', lat: coords[0], lng: coords[1] };
    }
  }
  return null;
}

// ── 구 이름 매칭 ──────────────────
function offlineGeocode(address: string): { lat: number; lng: number; name: string } | null {
  for (const [district, coords] of Object.entries(DISTRICT_COORDS)) {
    if (address.includes(district)) {
      return { lat: coords[0], lng: coords[1], name: district };
    }
  }
  return null;
}

function parseCoordValue(value: unknown): number | null {
  const num = Number(value);
  return Number.isFinite(num) ? num : null;
}

function pickKakaoAddressPayload(rawDocs: unknown[]): { lat: number; lng: number; address: string; road_address: string } | null {
  const doc = rawDocs[0];
  if (!doc || typeof doc !== 'object') return null;
  const row = doc as Record<string, unknown>;
  const lat = parseCoordValue(row.y);
  const lng = parseCoordValue(row.x);
  if (lat == null || lng == null) return null;

  const roadAddr = typeof row.road_address_name === 'string' ? row.road_address_name : '';
  const addr = typeof row.address_name === 'string' ? row.address_name : '';
  const place = typeof row.place_name === 'string' ? row.place_name : '';
  const roadFallback = typeof row.road_address === 'object' && row.road_address
    ? (row.road_address as Record<string, unknown>).address_name
    : '';

  const roadAddress = typeof roadFallback === 'string' ? roadFallback : roadAddr;
  const address = roadAddress || addr || place || '';
  if (!address) return null;

  return {
    lat,
    lng,
    address,
    road_address: roadAddress,
  };
}

async function fetchKakaoLocation(
  address: string,
  apiKey: string,
  queryType: 'keyword' | 'address',
): Promise<{ lat: number; lng: number; address: string; road_address: string; source: 'kakao' | 'kakao_address' } | null> {
  const endpoint = queryType === 'keyword'
    ? 'search/keyword.json'
    : 'search/address.json';

  const response = await fetch(
    `https://dapi.kakao.com/v2/local/${endpoint}?query=${encodeURIComponent(address)}`,
    { headers: { Authorization: `KakaoAK ${apiKey}` }, cache: 'no-store' },
  );

  if (!response.ok) return null;

  const payload = (await response.json().catch(() => null)) as { documents?: unknown[] } | null;
  const docs = Array.isArray(payload?.documents) ? payload.documents : [];
  const picked = pickKakaoAddressPayload(docs);
  if (!picked) return null;

  return {
    ...picked,
    source: queryType === 'keyword' ? 'kakao' : 'kakao_address',
  };
}

export async function GET(req: NextRequest) {
  const address = (req.nextUrl.searchParams.get('address') || '').trim();
  if (!address) {
    return NextResponse.json({ error: '주소를 입력하세요' }, { status: 400 });
  }

  const apiKey = (process.env.KAKAO_API_KEY || '').trim();

  // ── 1. 카카오 API ──
  if (apiKey) {
    try {
      const keywordResult = await fetchKakaoLocation(address, apiKey, 'keyword');
      if (keywordResult) {
        return NextResponse.json({
          ...keywordResult,
          source: 'kakao',
        });
      }

      const addressResult = await fetchKakaoLocation(address, apiKey, 'address');
      if (addressResult) {
        return NextResponse.json({
          ...addressResult,
          address: addressResult.address || address,
          source: 'kakao',
        });
      }
    } catch (e) {
      console.warn('카카오 API 실패:', e);
    }
  }

  // ── 2. 지하철역 DB ──
  const station = findStation(address);
  if (station) {
    return NextResponse.json({
      lat: station.lat,
      lng: station.lng,
      address: station.name,
      road_address: '',
      source: 'subway_db',
    });
  }

  // ── 3. 구 이름 오프라인 ──
  const district = offlineGeocode(address);
  if (district) {
    return NextResponse.json({
      lat: district.lat,
      lng: district.lng,
      address: address,
      road_address: '',
      source: 'offline_estimate',
      warning: '구 단위 근사 좌표',
    });
  }

  // ── 4. AI 변환 폴백 ──
  const aiResult = await parseWithAI(address);
  if (aiResult && aiResult.address) {
    // AI가 주소로 변환 → 다시 역/구 검색
    const aiStation = findStation(aiResult.address);
    if (aiStation) {
      return NextResponse.json({
        lat: aiStation.lat, lng: aiStation.lng,
        address: aiStation.name, road_address: '', source: 'ai+subway_db',
      });
    }
    const aiDistrict = offlineGeocode(aiResult.address);
    if (aiDistrict) {
      return NextResponse.json({
        lat: aiDistrict.lat, lng: aiDistrict.lng,
        address: aiResult.address, road_address: '', source: 'ai+offline',
      });
    }
  }

  return NextResponse.json({
    error: '주소를 찾을 수 없습니다. 구 이름이나 지하철역명을 포함해 주세요.',
  }, { status: 404 });
}
