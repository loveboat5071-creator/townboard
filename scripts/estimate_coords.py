def estimate_coords(city: str, district: str) -> tuple[float, float] | None:
    """도시+구 정보로 근사 좌표 반환"""
    # 직접 매칭
    if district in DISTRICT_COORDS:
        return DISTRICT_COORDS[district]
    # "서울특별시 마포구" → "마포구"
    for key, coords in DISTRICT_COORDS.items():
        if key in district or district in key:
            return coords
    return None


def main():
    with open(MASTER_JSON, "r", encoding="utf-8") as f:
        records = json.load(f)

    assigned = 0
    skipped = 0
    failed = 0

    for rec in records:
        # 이미 좌표가 있으면 스킵
        if rec.get("lat") and rec.get("lng"):
            skipped += 1
            continue

        coords = estimate_coords(rec.get("city", ""), rec.get("district", ""))
        if coords:
            # 같은 구 내에서도 약간의 분산을 주기 위해 해시 기반 오프셋
            name_hash = hash(rec.get("name", "") + rec.get("addr_road", ""))
            offset_lat = (name_hash % 1000) / 100000  # ~0.01도 = ~1km 범위
            offset_lng = ((name_hash >> 10) % 1000) / 100000
            rec["lat"] = round(coords[0] + offset_lat - 0.005, 6)
            rec["lng"] = round(coords[1] + offset_lng - 0.005, 6) 
            assigned += 1
        else:
            failed += 1

    with open(MASTER_JSON, "w", encoding="utf-8") as f:
        json.dump(records, f, ensure_ascii=False)

    print(f"✅ 좌표 추정 완료!")
    print(f"   할당: {assigned}건 | 기존 유지: {skipped}건 | 실패: {failed}건")
    print(f"   ⚠ 이 좌표는 구 단위 근사값입니다.")
    print(f"   ⚠ 정밀 좌표는 scripts/geocode_master.py로 카카오 API 사용 권장")


if __name__ == "__main__":
    main()
